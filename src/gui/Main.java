    void populateResidentProfile(int id) {
        Resident r = new ResidentFactory().getResident(id);

        r_fullname.setText(r.getFullName());
        r_gender.setSelectedItem(r.getR_gender());
        bdate.setText(r.getR_bday());
        r_age.setText("" + r.getAge());
        r_contact.setText(r.getR_contact());
        r_email.setText(r.getR_email());
        r_reiligion.setSelectedItem(r.getR_religion());
        r_civilstatus.setSelectedItem(r.getR_civilstatus());
        r_isVoter.setSelectedItem(r.getR_isvoter());
        r_status.setSelectedItem(r.getR_status());
        r_purok.setSelectedIndex(r.getR_purok());

        Official add = new OfficialFactory().getOfficial(r.getR_addedby());
        Official mod = new OfficialFactory().getOfficial(r.getR_modifiedby());

        r_addedby.setText(new ResidentFactory().getResident(add.getOff_r_id()).getFullName());
        r_addedon.setText(r.getR_addedon());
        r_modifiedby.setText(new ResidentFactory().getResident(mod.getOff_r_id()).getFullName());
        r_modifiedon.setText(r.getR_modifiedon());
    }
