    void updateOfficialTable() {

        DefaultTableModel model = (DefaultTableModel) table_officials.getModel();
        model.setRowCount(0);
        official_id = 0;

        for (Official f : new OfficialFactory().searchOfficial(search_official.getText())) {
            model.addRow(new Object[]{f.getOff_id(), new ResidentFactory().getResident(f.getOff_r_id()).getFullName(), f.getOff_position(), f.getOff_status()});
        }

    }
